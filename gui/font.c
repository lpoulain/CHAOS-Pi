int font_idx = -1;
Font *selected_font;

void set_font(int idx) {
    font_idx = idx;
    if (idx >= 0 && idx < 6) {
        selected_font = &fonts[idx];
    } else {
        selected_font = &fonts[0];
    }
}

uint get_font_height() {
    if (font_idx < 0) return 8;
    return selected_font->bottom - selected_font->top;
}

uint get_font_string_size(char *str) {
    int len = strlen(str);

    if (font_idx < 0) return 8 * len;

    uint size = 0;

    for (int i=0; i<len; i++) {
        unsigned char *f = (unsigned char *)((*selected_font->bitmap)[(uint8)str[i]]);
        size += f[34];
    }

    return size;
}

uint draw_font(unsigned char c, int x, int y) {
    if (font_idx >= 0) return draw_proportional_font(c, x, y);

    unsigned char *f = (unsigned char *)font[c];

    for (int j=0; j<8; j++) {
        unsigned char line = f[j];

        int mask = 128;
        for (int i=0; i<8; i++) {
            if (mask & line) {
                int off = (j+y)*pitch/4 + (i + x);
                buffer[off] = 0xFFFFFF;
            }
            mask /= 2;
        }
    }

    return 8;
}

uint draw_proportional_font(unsigned char c, int x, int y) {
    unsigned char *f = (unsigned char *)((*selected_font->bitmap)[c]);

    for (int j=1; j<16; j++) {
        unsigned char line = f[j*2];

        int mask = 128;
        for (int i=0; i<16; i++) {
            if (mask & line) {
                int off = (j+y)*pitch/4 + (i + x);
                buffer[off] = 0xFFFFFF;
            }
            mask /= 2;
        }

        line = f[j*2+1];

        mask = 128;
        for (int i=0; i<16; i++) {
            if (mask & line) {
                int off = (j+y)*pitch/4 + (i + x + 8);
                buffer[off] = 0xFFFFFF;
            }
            mask /= 2;
        }

    }

    return f[34];
}
